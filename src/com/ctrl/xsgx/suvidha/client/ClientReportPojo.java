package com.ctrl.xsgx.suvidha.client;

public class ClientReportPojo {

	private int clientId;
	private String clientName;
	private String clientMob;
	private String schemeName;
	private String cardNo;
	private String totalCollection;
	private String joinDate;
	private String frequency;
	
	
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getClientMob() {
		return clientMob;
	}
	public void setClientMob(String clientMob) {
		this.clientMob = clientMob;
	}
	public String getSchemeName() {
		return schemeName;
	}
	public void setSchemeName(String schemeName) {
		this.schemeName = schemeName;
	}
	public String getCardNo() {
		return cardNo;
	}
	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}
	public String getTotalCollection() {
		return totalCollection;
	}
	public void setTotalCollection(String totalCollection) {
		this.totalCollection = totalCollection;
	}
	public String getJoinDate() {
		return joinDate;
	}
	public void setJoinDate(String joinDate) {
		this.joinDate = joinDate;
	}
	public int getClientId() {
		return clientId;
	}
	public void setClientId(int clientId) {
		this.clientId = clientId;
	}
	public String getFrequency() {
		return frequency;
	}
	public void setFrequency(String frequency) {
		this.frequency = frequency;
	}
	
}
